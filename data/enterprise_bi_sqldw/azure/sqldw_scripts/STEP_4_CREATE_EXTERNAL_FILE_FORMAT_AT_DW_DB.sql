
IF EXISTS (SELECT * FROM SYS.external_file_formats where name ='UNCOMPRESSEDCSV')
BEGIN

	DROP EXTERNAL FILE FORMAT UNCOMPRESSEDCSV
END

CREATE EXTERNAL FILE FORMAT UNCOMPRESSEDCSV
WITH (
    FORMAT_TYPE = DELIMITEDTEXT,
    FORMAT_OPTIONS ( 
        FIELD_TERMINATOR = '|',
        STRING_DELIMITER = '',
        DATE_FORMAT = '',
        USE_TYPE_DEFAULT = False
    )
);

